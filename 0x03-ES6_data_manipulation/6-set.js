/**
 * Creates a set from a given array, eliminating duplicate elements.
 * @param {Array} array - The source array to convert into a set.
 * @author Ian Juma <https://github.com/Ianwaswa>
 * @returns {Set} A new set containing unique elements from the array.
 */
export default function setFromArray(array) {
  return new Set(array);
}
